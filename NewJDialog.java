    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try{
            Flor flor = createFlor();
            banco.insert(flor);
            florHelper.addFlor(flor);

            JOptionPane.showMessageDialog(this, "Flor incluída!");
        }
        catch(NullPointerException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Error: "+ e.getMessage()+ "\n\n"
                                          + "Certifique-se de que os campos abaixo foram preechidos:\n"+
                                            "- Comprimento Sepala\n"+
                                            "- Comprimento Petala\n"+
                                            "- Largura Sepala\n"+
                                            "- Largura Petala\n"+
                                            "- Classe");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            Flor flor = createFlor();
            banco.insert(flor);
            florHelper.alterar(flor);

            JOptionPane.showMessageDialog(this, "Flor atualizada!");
        }
        catch(NullPointerException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Error: "+ e.getMessage()+ "\n\n"
                                          + "Certifique-se de que os campos abaixo foram preechidos:\n"+
                                            "- Comprimento Sepala\n"+
                                            "- Comprimento Petala\n"+
                                            "- Largura Sepala\n"+
                                            "- Largura Petala\n"+
                                            "- Classe");
        }
    
    }//GEN-LAST:event_btnUpdateActionPerformed

