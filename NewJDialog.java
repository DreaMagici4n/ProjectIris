    public Flor createFlor(){
        Flor flor = new Flor(
                   Integer.parseInt(idInput.getText()),
                   Double.parseDouble(csInput.getText()),
                   Double.parseDouble(cpInput.getText()),
                   Double.parseDouble(lsInput.getText()),
                   Double.parseDouble(lpInput.getText()),
                   classInput.getText()
            );
        
        return flor;
    }
